public class WhileLoop {

    public static void main(String[] args) {

        // angka mulai
        int angka = 1;

        //Batasan Angka looping
        while (angka <= 10){
            //yang akan di looping
            System.out.println("Hadir Fadhlan : " + "Hadir Pak" );
            //operator penambahan
            angka++;
        }
    }
}
